export function getRandomFallbackVerse(excludeReferences: Set<string>): FallbackVerse {
  // Filter out recently used verses
  const availableVerses = fallbackVerses.filter(v => !excludeReferences.has(v.reference));
  
  // If all verses have been used recently, just use any verse
  const versesToChooseFrom = availableVerses.length > 0 ? availableVerses : fallbackVerses;
  
  // Get random verse
  const randomIndex = Math.floor(Math.random() * versesToChooseFrom.length);
  return versesToChooseFrom[randomIndex];
}
